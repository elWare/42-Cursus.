#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	index;

	index = 0;
	while (index < (unsigned int)ft_strlen(s))
	{
		(*f)(index, &s[index]);
		index++;
	}
}
