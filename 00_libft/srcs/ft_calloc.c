#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	ptr = malloc(nmemb * size);
	if (ptr == NULL)
		return (NULL);
	if (size != 0)
		ptr = ft_memset(ptr, 0, nmemb * size);
	return (ptr);
}
